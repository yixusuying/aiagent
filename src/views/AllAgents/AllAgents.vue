  } catch (err) {
    console.error('Ëé∑Âèñ‰∏™‰∫∫Êô∫ËÉΩ‰ΩìÊï∞ÊçÆÂ§±Ë¥•:', err)
    personalAgentsError.value = err.message || 'Ëé∑ÂèñÊï∞ÊçÆÂ§±Ë¥•'
  } finally {
    loadingPersonalAgents.value = false
  }
}

/**
 * ÁõëÂê¨TabÂàáÊç¢
 */
watch(activeTab, async (newTab, oldTab) => {
  if (newTab === oldTab) return
  
  if (newTab === TABS.OFFICIAL) {
    // ÊØèÊ¨°ÂàáÊç¢Âà∞ÂÆòÊñπÊé®ËçêÈÉΩÈáçÊñ∞Ëé∑ÂèñÊï∞ÊçÆ
